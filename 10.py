a=int(input('enter number 1: '))
b=int(input('enter number 2: '))

#After checking the condition, if the condition is met, the (if) block will be executed; if not, the else block will be executed
#پس از برسی شرط اگر شرط برقرار بود که بلوک شرط اجرا میشود اگر برقرار نبود دستور else  اجرا میشود


if a%b==0:
    print('a is divisible by b')
else:
    print('a is  not divisible by b')

