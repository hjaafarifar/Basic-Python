# Accessing the value key with a (for loop) is as follows
# دستیابی به کلید مقدار با حلقه (for) به صورت زیر است


# Get the keys
# به دست آوردن کلید ها
mydict={'a':15,'b':10,'c':4,'d':3.5}
for i in mydict:
    print(i)


