# A tuple is an ordered and immutable data type that is used to store multiple values
# تاپل نوعی داده مرتب و غیر قابل تغییر است که برای ذخیره چند مقدار استفاده میشود

mytuple=(5,'f',4,False,'S',4)
print('mytuple=',mytuple)
print(type(mytuple))

print('*'*50)
#Like the list, we do the following to get an element of the tuple
#مانند لیست برای دست یابی به عنصری از تاپل به صورت زیر عمل میکنیم

print('mytuple[3]=',mytuple[3])

print('*'*50)
# We use the internal function (len) to get the length of the tuple
# برای به دست اورد ن طول تاپل از تابع درونی (len) استفاده میکنیم

print('len(mytuple)=',len(mytuple))



